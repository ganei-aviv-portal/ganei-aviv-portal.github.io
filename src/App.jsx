function App() {
  const [lang, setLang] = useState('he');
  const [expandedStep, setExpandedStep] = useState(null);

  const t = translations[lang];

  useEffect(() => {
    document.documentElement.dir = lang === 'he' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }, [lang]);

  const toggleExpand = (id) => {
    setExpandedStep(expandedStep === id ? null : id);
  };

  return (
    <div className={`app-container ${lang}`}>
      <div className="language-switcher">
        <button 
          className={lang === 'he' ? 'active' : ''} 
          onClick={() => setLang('he')}
        >
          עברית
        </button>
        <button 
          className={lang === 'ru' ? 'active' : ''} 
          onClick={() => setLang('ru')}
        >
          Русский
        </button>
      </div>

      <header className="hero">
        <h1 className="title">{t.title}</h1>
        <p className="subtitle">{t.subtitle}</p>
      </header>

      <div className="steps-container">
        {t.steps.map((step, index) => (
          <div 
            key={step.id} 
            className={`step-card ${expandedStep === step.id ? 'expanded' : ''}`}
            style={{ animationDelay: `${index * 0.10}s` }}
          >
            <div className="step-header">
              <div className="step-number">{step.id}</div>
              {step.image && (
                <div className="step-image-container">
                  <img src={step.image} alt={step.title} className="step-image" />
                </div>
              )}
            </div>
            <div className="step-content">
              <h2 className="step-title">{step.title}</h2>
              <p className="step-description">{step.description}</p>
              
              <ul className="checklist">
                {step.checklist.map((item, i) => (
                  <li key={i} className="checklist-item">
                    <span className="checklist-icon">✓</span>
                    <span>{item}</span>
                  </li>
                ))}
              </ul>

              {step.extraInfo && (
                <div className="more-info-container">
                  <button 
                    className="read-more-btn"
                    onClick={() => toggleExpand(step.id)}
                  >
                    {expandedStep === step.id ? t.closeMore : t.readMore}
                  </button>
                  
                  <div className={`extra-info-panel ${expandedStep === step.id ? 'open' : ''}`}>
                    <div className="extra-info-content">
                      {step.extraInfo.note && (
                        <p className="highlight-note">
                          <strong>{t.highlightNote}</strong> {step.extraInfo.note}
                        </p>
                      )}
                      
                      {step.id === 4 && (
                        <>
                          <h4>{step.extraInfo.qTitle}</h4>
                          <p>{step.extraInfo.qText}</p>
                          <h4>{step.extraInfo.bringTitle}</h4>
                          <ul>
                            {step.extraInfo.bringItems.map((item, i) => <li key={i}>{item}</li>)}
                          </ul>
                        </>
                      )}

                      {step.id === 5 && (
                        <>
                          <h4>{step.extraInfo.liftTitle}</h4>
                          <p>{step.extraInfo.liftText}</p>
                          <h4>{step.extraInfo.elecTitle}</h4>
                          <p>{step.extraInfo.elecText}</p>
                        </>
                      )}

                      {step.id === 6 && (
                        <>
                          <h4>{step.extraInfo.title}</h4>
                          <ul>
                            {step.extraInfo.items.map((item, i) => <li key={i}>{item}</li>)}
                          </ul>
                        </>
                      )}

                      {step.id === 7 && (
                        <>
                          <h4>{step.extraInfo.title}</h4>
                          <p>{step.extraInfo.text}</p>
                        </>
                      )}
                    </div>
                  </div>
                </div>
              )}
            </div>
          </div>
        ))}
      </div>

      <footer className="download-section">
        <div className="download-card">
          <h3>{t.downloadTitle}</h3>
          <p>{t.downloadSubtitle}</p>
          <a href="/protocol-template.docx" download className="download-btn">
            <span className="download-icon">📥</span>
            {t.downloadBtn}
          </a>
        </div>
      </footer>
    </div>
  );
}

export default App;
